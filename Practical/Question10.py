#Program to print truth table for bitwise operators( &,| and ^ operators)
x=int(input("Enter the 1st number:"))
y=int(input("Enter the 2nd number:"))
a=x&y
b=x|y
c=x^y
print("Truth table for Bitwise AND operator is:",a)
print("Truth table for Bitwise OR operator is:",b)
print("Truth table for Bitwise XOR operator is:",c)
